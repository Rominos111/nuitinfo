	SOS Médecin<br/>
		SOS médecin est un service de médecine libérale 7/7 et 24H/24 qui permet en 	urgence de consulter un médecin adapté, ce service n’est pas gratuit mais est remboursé si 	l’on possède une mutuelle et une couverture sociale.<br/>
		Lien SOS Médecin : https://sosmedecins-france.fr/<br/>
	
	Médecine préventive<br/>
		Souvent les universités proposent des consultations médicales à la médecine 	préventive sur lesquelles s’appliquent le tiers payant, ce qui permet de n’avoir aucun frais à 	avancer si l’on est couvert par la sécurité sociale et une mutuelle (attention afin de pouvoir 	bénéficier de cela il faut avoir un médecin traitant déclaré).<br/>

	Consultations à l’hôpital et urgences<br/>
		Les hôpitaux bénéficiant également du tiers payant il n’y a généralement pas de 	frais à avancer (si sécurité sociale et mutuelle il y a).
	
