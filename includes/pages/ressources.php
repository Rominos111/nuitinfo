	Fonctionnement :<br/>
	-un.e infirmier.ère va vous demander si vous désirez faire le dépistage de manière anonyme ou non, si vous décidez de conserver l’anonymat elle prendra uniquement quelques informations (dates de naissance notamment) puis vous donnera un numéro d’identification à conserver afin de récupérer les résultats. Dans le cas où vous ne désirerez pas être anonymisé (sachant que les résultats et entretiens tiennent du secret médical) il vous suffira de présenter un papier d’identité pour récupérer les résultats.
A la suite de cette accueil vous serez envoyé vers le médecin du CDAG qui en entretient privé vous posera des questions concernant votre santé, vos vaccins et vos potentielles prises de risques/pratiques à risque. Il définira les pathologies à dépister et vous renverra vers un.e infirmier.ère qui effectuera le prélèvement.<br/>
Enfin une semaine plus tard les résultats seront disponibles et seront ouverts en présence d’un-- médecin en entretient privé.<br/>

